atomic_number = 2
if (atomic_number > 1) and (atomic_number < 5):
    print('(atomic_number > 1) and (atomic_number < 5)')


# the or expression
if (atomic_number > 1) or (atomic_number < 5):
    print('(atomic_number > 1) or (atomic_number < 5)')


